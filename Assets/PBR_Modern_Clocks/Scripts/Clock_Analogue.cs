using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Clock_Analogue : MonoBehaviour {
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
//
//                                          Simple Clock Script by Andre "AEG" Bürger /VIS-Games (2021)
//  
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------

    //-------------------------------------------------        
    public float speed = 1.0f;
    public int second;      // start Time 
    public int minute;      // start Time
    public int hour;        // start Time
    //-------------------------------------------------
    Transform handleSecond;
    Transform handleMinute;
    Transform handleHour;
    //-------------------------------------------------
    float delay;
    //-------------------------------------------------

//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
void Start()
{
    handleSecond = transform.Find("hand_second");
    handleMinute = transform.Find("hand_minute");
    handleHour   = transform.Find("hand_hour");
}
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
void Update()
{
    //--------------------------------------------------------------------------------------------------------------------------
    delay -= Time.deltaTime * speed;
    if(delay < 0.0f)
    { 
        delay = 1.0f;
        second++;
        if(second >= 60)
        {
            second = 0;
            minute++;
            if(minute >= 60)
            {
                minute = 0;
                hour++;
                if(hour >= 12)
                    hour = 0;
            }
        }
        handleSecond.localEulerAngles = new Vector3(0.0f, 0.0f,  ((360.0f / 60.0f) * second) );
        handleMinute.localEulerAngles = new Vector3(0.0f, 0.0f,  ((360.0f / 60.0f) * minute) );
        handleHour.localEulerAngles   = new Vector3(0.0f, 0.0f, ((360.0f / 12.0f) * hour) + ((360.0f / (60.0f * 12.0f)) * minute) );
    }
    //--------------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------------------------
}
